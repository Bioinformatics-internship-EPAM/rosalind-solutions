stop_codones = ['UAA', 'UAG', 'UGA']

rna_string = ''
with open('rosalind_prot.txt', 'r') as inp_file:
    rna_string = inp_file.read()[:-1]
rna_separated = [rna_string[i: i+3] for i in range(0, len(rna_string), 3)]
print(rna_separated)
protein = ''
for cdn in rna_separated:
    if cdn not in stop_codones:
        protein += codon_table[cdn]
    else:
        break
print(protein)
